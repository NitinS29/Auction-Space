CREATE TABLE items (item_display_name VARCHAR(500), price FLOAT, quantity INT, start_time DATE, end_time DATE, location VARCHAR(100), description VARCHAR(1000));

CREATE TABLE User (
fname varchar(25),
mname varchar(25),
lname varchar(25),
emailId varchar(30),
username varchar(40),
password varchar(40),
phone int,
address varchar(50),
userType varchar(10)
);